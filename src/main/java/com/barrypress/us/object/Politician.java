package com.barrypress.us.object;

public class Politician {

    private String name;
    private Class ability;
    private Class bonus;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Class getAbility() {
        return ability;
    }

    public void setAbility(Class ability) {
        this.ability = ability;
    }

    public Class getBonus() {
        return bonus;
    }

    public void setBonus(Class bonus) {
        this.bonus = bonus;
    }
}
